            querys.setTelefono(telEmp);
            querys.setCargo(Cargo);
            querys.setNombreEmergencia(nomEme);
            querys.setTelefonoemergencia(telEme);
            querys.setDireccion(texdire);
            querys.setEstado(estaEmp);
            


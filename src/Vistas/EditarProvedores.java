    private void fmt_telefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fmt_telefonoKeyTyped
      
    String currentText = fmt_telefono.getText();
    char validar = evt.getKeyChar();

    // Validación para que no acepte letras
    if (Character.isLetter(validar)) {
        getToolkit().beep();
        evt.consume();
    }

    // Validación para teléfonos específicos
    Set<String> telefonosProhibidos = new HashSet<>(Arrays.asList("0000-0000", "01", "4", "5", "6", "7"));

    for (String telefonoProhibido : telefonosProhibidos) {
        if (currentText.startsWith(telefonoProhibido)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this, "solo se permiten los numeros que empiezan con 2, 3, 8 y 9");
            break;
        }
    }


    }//GEN-LAST:event_fmt_telefonoKeyTyped


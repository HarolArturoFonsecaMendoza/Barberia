    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        
           int idTxt = Integer.parseInt(txtID.getText().trim());
        
        String nomProv = txt_nombre.getText().trim(); // Eliminar espacios en blanco al inicio y al final
       
        if (nomProv.isEmpty()) {
             JOptionPane.showMessageDialog(null, "El nombre del representante no puede estar vacío", "Error de validación", JOptionPane.WARNING_MESSAGE);
            return; // Salir del método si el campo está vacío
        }
        if (!nomProv.matches("[a-zA-Záéíóúñ][a-zA-Z áéíóúñ]+")) {
             JOptionPane.showMessageDialog(null, "El nombre del representante solo puede contener letras", "Error de validación", JOptionPane.WARNING_MESSAGE);
             return; // Salir del método si el nombre contiene caracteres no válidos
        }
        
         String empProv = txt_empresa.getText().trim(); // Eliminar espacios en blanco al inicio y al final
        if (empProv.isEmpty()) {
              JOptionPane.showMessageDialog(null, "El nombre de la empresa no puede estar vacío", "Error de validación", JOptionPane.WARNING_MESSAGE);
             return; // Salir del método si el campo está vacío
        }
        if (!empProv.matches("[a-zA-Záéíóúñ.][a-zA-Záéíóúñ 0-9.]+")) {
             JOptionPane.showMessageDialog(null, "El nombre de la empresa solo puede contener letras", "Error de validación", JOptionPane.WARNING_MESSAGE);
             return; // Salir del método si el nombre contiene caracteres no válidos
        }
       
        String rtnPro = fmt_rtn.getText(); // Eliminar espacios en blanco al inicio y al final
      
        String telProv = fmt_telefono.getText(); // Eliminar espacios en blanco al inicio y al final
        if (telProv.isEmpty()) {
             JOptionPane.showMessageDialog(null, "El telefono no puede esta vacío", "Error de validación", JOptionPane.WARNING_MESSAGE);
             return; // Salir del método si el campo está vacío
        }
         if (!telProv.matches("[0-9][-0-9]+")) {
           JOptionPane.showMessageDialog(null, "El telefono no puede esta vacío", "Error de validación", JOptionPane.WARNING_MESSAGE);
              return; // Salir del método si el nombre contiene caracteres no válidos
        }
       
       if (telProv.equals("0000-0000")) {
            JOptionPane.showMessageDialog(null, "El telefono no puede ser cero", "Error de validación", JOptionPane.WARNING_MESSAGE);
               return; // Salir del método si el teléfono no es válido
         }
              
                 
         String desProv = txta_direccion.getText().trim(); // Eliminar espacios en blanco al inicio y al final
        if (desProv.isEmpty()) {
          JOptionPane.showMessageDialog(null, "La direccion no puede estar vacía", "Error de validación", JOptionPane.WARNING_MESSAGE);
              return; // Salir del método si el campo está vacío
        }
        if (!desProv.matches("[a-zA-Záéíóúñ.,;:-_ 0-9][a-zA-Záéíóúñ.,;.;-_ 0-9]+")) {
            JOptionPane.showMessageDialog(this, "La dirección solo puede contener letras y numeros");
            return; // Salir del método si el nombre contiene caracteres no válidos
        }
        
        
            // Crear una instancia de QuerysProductos
            QuerysProveedores querys = new QuerysProveedores();
            querys.setNombre(nomProv);
            querys.setEmpresa(empProv);
            querys.setRtn(rtnPro);
            querys.setTelefono(telProv);
            querys.setDireccion(desProv);
            querys.setId(idTxt);
            // Llamar al método Guardar de la clase Productos para guardar los datos
            if (Proveedores.Editar(querys)) {
                JOptionPane.showMessageDialog(null, "Provedor actualizado con éxito");
            Proveedores.MostrarProvedores("",NORMAL);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Algo falló, consulte con el administrador del sistema", "Error al guardar", JOptionPane.OK_OPTION);
          }
    

    }//GEN-LAST:event_btn_guardarActionPerformed


